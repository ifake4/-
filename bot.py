@bot.callback_query_handler(func=lambda call: True)
def handle_callback(call):
    """Обработка нажатий на inline кнопки"""
    chat_id = call.message.chat.id
    message_id = call.message.message_id
    
    try:
        if call.data == "main_menu":
            bot.delete_message(chat_id, message_id)
            bot.send_message(
                chat_id,
                "🏠 Главное меню\nВыберите раздел:",
                reply_markup=get_main_keyboard()
            )
            
        elif call.data == "back_to_sections":
            bot.edit_message_text(
                "📚 Выберите раздел:",
                chat_id,
                message_id,
                reply_markup=get_sections_keyboard()
            )
            
        elif call.data.startswith("section_"):
            section_id = int(call.data.split("_")[1])
            show_section(chat_id, section_id)
            
        elif call.data.startswith("page_"):
            parts = call.data.split("_")
            section_id = int(parts[1])
            page = int(parts[2])
            
            section = next((s for s in sections if s["id"] == section_id), None)
            if section:
                text = f"{section['icon']} **{section['name']}**\nВыберите вопрос:"
                bot.edit_message_text(
                    text,
                    chat_id,
                    message_id,
                    parse_mode='Markdown',
                    reply_markup=get_questions_keyboard(section_id, page)
                )
                
        elif call.data.startswith("question_"):
            question_id = int(call.data.split("_")[1])
            
            # Ищем вопрос по id
            found = False
            for section in sections:
                for question in section.get("questions", []):
                    if question["id"] == question_id:
                        found = True
                        # Форматируем ответ
                        answer_text = f"❓ **{question['question']}**\n\n📌 **Ответ:**\n{question['answer']}"
                        
                        # Добавляем дисклеймер
                        answer_text += "\n\n---\n_Данная информация носит справочный характер. Для решения конкретной ситуации рекомендуется обратиться к юристу._"
                        
                        # Находим id раздела для кнопки возврата
                        section_id = section["id"]
                        
                        keyboard = types.InlineKeyboardMarkup()
                        keyboard.add(
                            types.InlineKeyboardButton("🔙 К списку вопросов", callback_data=f"section_{section_id}"),
                            types.InlineKeyboardButton("🏠 Главное меню", callback_data="main_menu")
                        )
                        
                        bot.edit_message_text(
                            answer_text,
                            chat_id,
                            message_id,
                            parse_mode='Markdown',
                            reply_markup=keyboard
                        )
                        break
                if found:
                    break
            
            if not found:
                bot.answer_callback_query(call.id, "Вопрос не найден")
                
    except Exception as e:
        logger.error(f"Ошибка в callback: {e}")
        bot.answer_callback_query(call.id, "Произошла ошибка")

